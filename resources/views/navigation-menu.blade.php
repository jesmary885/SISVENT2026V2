    document.addEventListener('DOMContentLoaded', function() {
        const hamburgerButton = document.querySelector('[x-data="{ open: false }"]');
        
        if (hamburgerButton) {
            const responsiveLinks = document.querySelectorAll('.responsive-nav-link');
            
            responsiveLinks.forEach(link => {
                link.addEventListener('click', function() {
                    // Solo cerrar en móviles
                    if (window.innerWidth < 640) {
                        hamburgerButton.__x.$data.open = false;
                    }
                });
            });
        }
    });
</script>